#the range() funtion

range(7)#this generates the sequence (0, 1, 2, 3, 4, 5, 6)

range(0, 7, 1)#this is the same as the last on but you are able to determine the start, stop, and step.

range(2, 14, 2)#this will start at 2, stoping before 14 and go by 2. (2, 4, 6, 8, 10, 12)


#this let's us loop a certain number of times.
